# Ввід числа N
N = int(input("Введіть число N (1 < N < 9): "))

# Перевірка умови
if N <= 1 or N >= 9:
    print("Помилка: N повинно бути в діапазоні 1 < N < 9")
else:
    # Формування рисунку
    for i in range(1, N + 1):
        for j in range(1, i + 1):
            print(j, end=" ")
        print()  # Перехід на новий рядок
