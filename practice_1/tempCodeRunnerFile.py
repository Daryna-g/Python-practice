# Ввід даних
a = float(input("Введіть число a (додатнє): "))
b = float(input("Введіть число b (додатнє): "))

# Перевірка на додатність
if a <= 0 or b <= 0:
    print("Помилка: числа a та b повинні бути додатними!")
else:
    # Обчислення X за умовами
    if a < b:
        X = a / b + 5
    elif a == b:
        X = -5
    else:  # a > b
        X = (a * a - b) / b

    # Вивід результату
    print("Результат X =", X)