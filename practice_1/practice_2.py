# Програма: перші 8 чисел ряду Фібоначчі та їх сума

# Створюємо список для збереження чисел Фібоначчі
fib = [0, 1]

# Генеруємо ще 6 чисел
for i in range(2, 8):
    fib.append(fib[i - 1] + fib[i - 2])

# Знаходимо суму чисел
fib_sum = sum(fib)

# Виводимо результат
print("Перші 8 чисел ряду Фібоначчі:", fib)
print("Сума цих чисел =", fib_sum)
